"""
Pydantic models shared across the application.

Organised by domain:
  - Auth         (users, tokens)
  - SoW          (documents, sections)
  - Content      (content, scope, pricing, assumptions, resources)
  - AI Suggestion
  - History / Collaboration
"""

from __future__ import annotations

from datetime import datetime
from typing import Any

from pydantic import BaseModel, EmailStr, Field

# ── Auth ─────────────────────────────────────────────────────────────────────


class UserCreate(BaseModel):
    """Payload for registering a new user."""

    email: EmailStr
    password: str = Field(min_length=8, description="Minimum 8 characters")
    full_name: str | None = None
    username: str | None = None


class UserLogin(BaseModel):
    """Payload for logging in."""

    email: EmailStr
    password: str


class UserResponse(BaseModel):
    """Safe representation of a user (no password)."""

    id: int
    email: str
    full_name: str | None
    username: str | None = None
    name: str | None = None
    role: str
    is_active: bool
    created_at: datetime


class Token(BaseModel):
    """JWT bearer token returned after successful login."""

    access_token: str
    token_type: str = "bearer"
    user: UserResponse


class TokenData(BaseModel):
    """Claims extracted from a decoded JWT."""

    user_id: int | None = None
    email: str | None = None


# ── Content child-table models  (PDF §2.2.1 – 2.2.4) ─────────────────────────


class ScopeRecord(BaseModel):
    """Scope table row — PDF §2.2.1."""

    id: int
    content_id: int | None = None
    in_scope: list[Any] | None = None
    out_scope: list[Any] | None = None


class PricingRecord(BaseModel):
    """Pricing table row — PDF §2.2.2."""

    id: int
    content_id: int | None = None
    total_value: float | None = None
    breakdown: list[Any] | None = None


class AssumptionsRecord(BaseModel):
    """Assumptions table row — PDF §2.2.3."""

    id: int
    content_id: int | None = None
    items: list[Any] | None = None


class ResourcesRecord(BaseModel):
    """Resources table row — PDF §2.2.4."""

    id: int
    content_id: int | None = None
    team: list[Any] | None = None


class ContentRecord(BaseModel):
    """Content table row — PDF §2.2."""

    id: int
    scope_id: int | None = None
    price_id: int | None = None
    assumption_id: int | None = None
    resource_id: int | None = None


# ── AI Suggestion  (PDF §2.3) ────────────────────────────────────────────────


class AISuggestion(BaseModel):
    """AI Suggestion table row — PDF §2.3."""

    id: int
    flag: str | None = None  # Green / Yellow / Red
    validation_recommendation: dict[str, Any] | None = None
    risks: list[Any] | None = None


# ── SoW ──────────────────────────────────────────────────────────────────────


class SoWCreate(BaseModel):
    """Payload for creating a new SoW document.

    ``cycle`` tracks which deal cycle (1–4) the SoW is in (PDF §2.1).
    ``client_id`` is the optional string ID generated by the frontend for
    backward-compatibility with localStorage state; omit it when the frontend
    posts to the backend first (the preferred flow).
    """

    title: str = Field(min_length=1)
    cycle: int | None = Field(
        default=1,
        ge=1,
        le=4,
        description="Deal cycle 1–4 (PDF §2.1)",
    )
    client_id: str | None = Field(
        default=None,
        description="Frontend-generated string ID (legacy/offline fallback)",
    )
    methodology: str | None = None
    customer_name: str | None = None
    opportunity_id: str | None = None
    deal_value: float | None = None
    content: dict[str, Any] | None = None
    metadata: dict[str, Any] | None = None


class SoWUpdate(BaseModel):
    """Partial update payload — any field is optional."""

    title: str | None = Field(default=None, min_length=1)
    cycle: int | None = Field(default=None, ge=1, le=4)
    status: str | None = None
    methodology: str | None = None
    customer_name: str | None = None
    opportunity_id: str | None = None
    deal_value: float | None = None
    content: dict[str, Any] | None = None
    metadata: dict[str, Any] | None = None


class SoWStatusUpdate(BaseModel):
    """Payload for updating only the SoW status."""

    status: str = Field(description="e.g. draft, in_review, approved, rejected")


class SoWResponse(BaseModel):
    """Full SoW document representation returned from the API."""

    id: int
    title: str
    status: str
    cycle: int | None = None
    content_id: int | None = None
    ai_suggestion_id: int | None = None
    uploaded_at: datetime
    updated_at: datetime
    client_id: str | None = None
    methodology: str | None = None
    customer_name: str | None = None
    opportunity_id: str | None = None
    deal_value: float | None = None
    content: dict[str, Any] | None = None
    metadata: dict[str, Any] | None = None


class SoWSummary(BaseModel):
    """Lightweight SoW entry returned in list responses."""

    id: int
    title: str
    status: str
    cycle: int | None = None
    methodology: str | None = None
    customer_name: str | None = None
    opportunity_id: str | None = None
    deal_value: float | None = None
    client_id: str | None = None
    updated_at: datetime


# ── History / Collaboration  (PDF §2.4, §2.5) ────────────────────────────────


class HistoryEntry(BaseModel):
    """History audit log row — PDF §2.4."""

    id: int
    sow_id: int | None = None
    changed_by: int | None = None
    change_type: str | None = None
    changed_at: datetime
    diff: dict[str, Any] | None = None


class CollaborationEntry(BaseModel):
    """Collaboration (user↔SOW role) row — PDF §2.5."""

    id: int
    sow_id: int | None = None
    user_id: int
    role: str | None = None
