# ── SoW ──────────────────────────────────────────────────────────────────────


class SoWCreate(BaseModel):
    """Payload for creating a new SoW document.

    ``cycle`` tracks which deal cycle (1–4) the SoW is in (PDF §2.1).
    ``client_id`` is the optional string ID generated by the frontend for
    backward-compatibility with localStorage state; omit it when the frontend
    posts to the backend first (the preferred flow).
    """

    title: str = Field(min_length=1)
    cycle: int | None = Field(
        default=1,
        ge=1,
        le=4,
        description="Deal cycle 1–4 (PDF §2.1)",
    )
    client_id: str | None = Field(
        default=None,
        description="Frontend-generated string ID (legacy/offline fallback)",
    )
    methodology: str | None = None
    customer_name: str | None = None
    opportunity_id: str | None = None
    deal_value: float | None = None
    estimated_margin: float | None = None
    content: dict[str, Any] | None = None
    metadata: dict[str, Any] | None = None
    workflow_template_id: int | None = Field(
        default=None,
        description="Workflow template ID; defaults to the system default template",
    )
    content_template_id: int | None = Field(
        default=None,
        description="Content template ID for pre-populated section content",
    )


